	function generateTable(arrayName) {
		// Create an empty string to hold the table output
		let output = "";

		// Create the table header
		output += "<table>";
		output += "<tr>";
		output += "<th>Course Title:</th>";
		output += "<th>Where I am now:</th>";
		output += "<th>How many lessons:</th>";
		output += "<th>Progress of execution:</th>";
		output += "</tr>";

		// Loop through courses array to create table rows
		for(let i = 0; i < arrayName.length; i++) {
			// Set up the color depending on the progress
			let color;
			if (arrayName[i].progress === "Not started!") {
				color = "#ed438a";
			} else if (arrayName[i].progress === "In progress...") {
				color = "#263773";
			} else {
				color = "#d19fe3";
			}

			output += "<tr style='color:" + color + ";'>";
			output += "<td>" + arrayName[i].title + "</td>";
			output += "<td>" + arrayName[i].whereNow + "</td>";
			output += "<td>" + arrayName[i].howMany + "</td>";
			output += "<td>" + arrayName[i].progress + "</td>";
			output += "</tr>";
		}
		output += "</table>";

		document.write(output);
	}
